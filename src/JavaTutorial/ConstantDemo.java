package JavaTutorial;

public class ConstantDemo {

	public static void main(String[] args) {
		float a = 2.2F;
		long b = 2147383648L;
		byte c = 100;
		short d = 200;
		
	
	}

	/* 상수 뒤에 F 붙이기
	 * : float형이라는 것을 명시적으로 표시
	 * 
	 * 실수인 상수의 기본 데이터 형
	 * : double
	 * 
	 * 상수 뒤에 L 붙이기
	 * : long형이라는 것을 명시적으로 표시
	 * 
	 * byte나 short 형인 경우
	 * : 형변환을 안해줘도 자동으로 변환 가능!!
	 */
}

